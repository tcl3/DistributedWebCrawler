        public const int MillisecondsPerMinute = 60000;
        public const int MillisecondsPerHour = MillisecondsPerMinute * 60;
        public const int MillisecondsPerDay = MillisecondsPerHour * 24;
        public const int MillisecondsPerWeek = MillisecondsPerDay * 7;
    }
}
